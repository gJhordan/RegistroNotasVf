    private void TblPeriodosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblPeriodosMousePressed
        fila = TblPeriodos.getSelectedRow();

        if (fila != -1) {
            CmbEstadoSelect.removeAllItems();
            String Nombre = TblPeriodos.getValueAt(fila, TblPeriodos.getColumnModel().getColumnIndex("Nombre")).toString();
            String estado = TblPeriodos.getValueAt(fila, TblPeriodos.getColumnModel().getColumnIndex("Estado")).toString();

//            String Nombreproximo = TblPeriodos.getValueAt(fila + 1, TblPeriodos.getColumnModel().getColumnIndex("Nombre")).toString();
            //    String estadoproximo = TblPeriodos.getValueAt(fila + 1, TblPeriodos.getColumnModel().getColumnIndex("Estado")).toString();
            if (Nombre.equals(VEPST.getNombrep())) {
                CmbEstadoSelect.setEnabled(true);
                LblNombreSelect.setText(Nombre);
                if (estado.equals("Cerrado")) {
                    CmbEstadoSelect.addItem("Cerrado");
                    CmbEstadoSelect.addItem("Prematricula");
                } else {
                    if (estado.equals("Prematricula")) {
                        CmbEstadoSelect.addItem("Cerrado");
                        CmbEstadoSelect.addItem("Prematricula");
                        CmbEstadoSelect.addItem("Matricula Abierta");
                        CmbEstadoSelect.setSelectedIndex(1);
                    } else {
                        if (estado.equals("Matricula Abierta")) {
                            CmbEstadoSelect.addItem("Matricula Abierta");
                            CmbEstadoSelect.addItem("En Curso");
                        } else {
                            if (estado.equals("En Curso")) {
                                CmbEstadoSelect.addItem("En Curso");
                                CmbEstadoSelect.addItem("Finalizado");
                            }
                        }
                    }
                }

            } else {
                if (estado.equals("Finalizado")) {
                    JOptionPane.showMessageDialog(null, "Ya finalizÃ³ este periodo, no puede manipular este periodo");
                } else {
                    if ((estado.equals("Cerrado") || estado.equals("Prematricula")) && E.ValidarMatricula() == 0) {
                        CmbEstadoSelect.setEnabled(true);
                        LblNombreSelect.setText(Nombre);
                        if (estado.equals("Cerrado")) {
                            CmbEstadoSelect.addItem("Cerrado");
                            CmbEstadoSelect.addItem("Prematricula");
                        } else {
                            if (estado.equals("Prematricula")) {

                                CmbEstadoSelect.addItem("Prematricula");
                                CmbEstadoSelect.addItem("Cerrado");
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "El periodo actual ya esta en funcionamiento \n no puede realizar cambios en otros periodos hasta que el actual finalice");
                    }
                }

            }

        }
    }//GEN-LAST:event_TblPeriodosMousePressed

    private void CmbEstadoSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CmbEstadoSelectActionPerformed
        int OpcionesEstado = (int) CmbEstadoSelect.getItemCount();
        String estadoactual;
        if (OpcionesEstado > 2) {
            estadoactual = (String) CmbEstadoSelect.getItemAt(1);
        } else {
            estadoactual = (String) CmbEstadoSelect.getItemAt(0);
        }

        String nuevoestado = (String) CmbEstadoSelect.getSelectedItem();

        if (estadoactual != null) {
            if (estadoactual.equals(nuevoestado)) {
                BtnGuardar.setEnabled(false);
            } else {
                BtnGuardar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_CmbEstadoSelectActionPerformed

    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        String nombreactual = LblNombreSelect.getText();
        String estadoactual = (String) CmbEstadoSelect.getSelectedItem();
        E.CambiarPeriodo(nombreactual, estadoactual);
        if (estadoactual.equals("En Curso")) {
            E.SeccionesxNotasTipoParticipacion();
        }
        if (estadoactual.equals("Finalizado")) {
            E.actualizarCiclo();
            E.actualizarBecas();
            E.actualizarPagos();
            E.actualizarSecciones();
}
        
        LblNombreSelect.setText("-");
        CmbEstadoSelect.removeAllItems();
        CmbEstadoSelect.setEnabled(false);
        TblPeriodos.removeAll();
        Tbl.CargarTablaPeriodos(TblPeriodos, "periodos", "");
        E.DefinirPeriodo();
    }//GEN-LAST:event_BtnGuardarActionPerformed


