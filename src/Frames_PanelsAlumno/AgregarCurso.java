    class ButtonEditor extends DefaultCellEditor {

        private final JButton button;
        private String label;
        private boolean isPushed;

        public ButtonEditor(JCheckBox checkBox) {
            super(checkBox);
            button = new JButton();
            button.setOpaque(true);
            button.setBackground(Color.GREEN);
            button.setForeground(Color.WHITE);
            button.addActionListener(e -> fireEditingStopped());
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            label = "Seleccionar";
            button.setText(label);
            isPushed = true;
            return button;
        }

        @Override
        public Object getCellEditorValue() {
            if (isPushed) {
                b = 0;
                for (int i = 0; i < VEMS.getHorarioF().length; i++) {
                    if (tempHT.containsKey(i)) {
                        String contenido = tempHT.get(i);

                        if (contenido.equals(h1) || contenido.equals(h2)) {
                            JOptionPane.showMessageDialog(null, "Existe cruce de Horario si escoges este horario\nEscoge otro horario");
                            b = 1;
                        }
                    }
                }
                if (b == 0) {
                    DAOM.AgregarSeccionesMatriculadas(M);
                    PanelCursoMatricular pCM = new PanelCursoMatricular();
                    mostrarTipo(pCM);
                }
            }
            isPushed = false;
            return label;
        }

        @Override
        public boolean stopCellEditing() {
            isPushed = false;
            return super.stopCellEditing();
        }
    }

