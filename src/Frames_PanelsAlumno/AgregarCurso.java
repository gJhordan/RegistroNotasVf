    private void tblAgregarCursoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAgregarCursoMousePressed

        int fila = tblAgregarCurso.getSelectedRow();

        if (fila != -1) {
            String Seccion = tblAgregarCurso.getValueAt(fila, tblAgregarCurso.getColumnModel().getColumnIndex("Seccion")).toString();
            String horarioconca = tblAgregarCurso.getValueAt(fila, tblAgregarCurso.getColumnModel().getColumnIndex("Horario")).toString();
            M.setNroseccion(Integer.parseInt(Seccion));
            if (horarioconca.contains(" y ")) {
                String[] partes = horarioconca.split(" y ");
                h1 = partes[0];
                h2 = partes[1];
            } else {
                h1 = horarioconca;
            }

        }

        tblAgregarCurso.getColumnModel().getColumn(tblAgregarCurso.getColumnCount() - 1).setCellRenderer(new Render());
        tblAgregarCurso.getColumnModel().getColumn(tblAgregarCurso.getColumnCount() - 1).setCellEditor(new ButtonEditor(new JCheckBox()));
    }//GEN-LAST:event_tblAgregarCursoMousePressed

