    public PanelDetallesCurso() {
        initComponents();
        btnSimuPond.setEnabled(false);
        ADC.setAlumnoID(Usua.getID());
        DAOADC.CargarPeriodosxAlumno(ADC, CmbPeriodosxCursos);
        
//        LblNroCiclo.setText("" + Alua.getCiclo());
        if (CmbPeriodosxCursos.getItemCount() > 0) {
             
            DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) CmbPeriodosxCursos.getModel();

            for (int i = 0; i < model.getSize(); i++) {
                String elemento = model.getElementAt(i);
                if (elemento.equals(VEPST.getNombrep())) {
                    val++;
                }
            }
            if (val > 0) {
                CmbPeriodosxCursos.setSelectedItem(VEPST.getNombrep());
                ADC.setNomPeriodo(VEPST.getNombrep());
                DAOADC.CargarSeccionesxAlumno(ADC, tblDetallesCurso);
                String Cursonom = tblDetallesCurso.getValueAt(0, tblDetallesCurso.getColumnModel().getColumnIndex("Curso")).toString();
                int a = HS.CargarCiclo(Cursonom);
                LblNroCiclo.setText("" + a);
            } else {
                contar = CmbPeriodosxCursos.getItemCount();
                CmbPeriodosxCursos.setSelectedIndex((contar - 1));
                ADC.setNomPeriodo((String) CmbPeriodosxCursos.getItemAt((contar - 1)));
                DAOADC.CargarSeccionesxAlumno(ADC, tblDetallesCurso);
                String Cursonom = tblDetallesCurso.getValueAt(0, tblDetallesCurso.getColumnModel().getColumnIndex("Curso")).toString();
                int a = HS.CargarCiclo(Cursonom);
                LblNroCiclo.setText("" + a);
            }
              DAOADC.GuardarPromedios(ADC);
                if (Existe(ADC.getPonderado())) {
                LblPonderado.setText(Double.toString(ADC.getPonderado()));
            } else {
                LblPonderado.setText("-");
            }
            Val2 = 1;
        } else {
            jPanel3.setEnabled(false);
            jPanel3.setVisible(false);
            JOptionPane.showMessageDialog(null, "No tiene cursos actualmente en ningun periodo.");
            PanelLimpiarAlumno pLA = new PanelLimpiarAlumno();
            mostrarTipo(pLA);
        }
    }

