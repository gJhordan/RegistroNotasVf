    private void tblDetallesCursoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDetallesCursoMousePressed
        int fila = tblDetallesCurso.getSelectedRow();

        if (fila != -1) {
            String SeccionS = tblDetallesCurso.getValueAt(fila, tblDetallesCurso.getColumnModel().getColumnIndex("Seccion")).toString();
            int Seccion = Integer.parseInt(SeccionS);
            String H = HS.CargarHorario(Seccion, SeccionS);
            lblHorario.setText(H);
            ADC.setNroseccionAl(Integer.parseInt(SeccionS));
            DAOADC.CargarNotasxSeccion(ADC);
            LblNota1N.setText(ADC.getNotasNombres(0));
            LblNota2N.setText(ADC.getNotasNombres(1));
            LblNota3N.setText(ADC.getNotasNombres(2));
            LblNota4N.setText(ADC.getNotasNombres(3));
            LblNota1Por.setText("" + ADC.getNotasPorcentajes(0) + "%");
            LblNota2Por.setText("" + ADC.getNotasPorcentajes(1) + "%");
            LblNota3Por.setText("" + ADC.getNotasPorcentajes(2) + "%");
            LblNota4Por.setText("" + ADC.getNotasPorcentajes(3) + "%");
            if (Existe(ADC.getNotas(0))) {
                LblNota1.setText(Double.toString(ADC.getNotas(0)));
            } else {
                LblNota1.setText("-");
            }
            if (Existe(ADC.getNotas(1))) {
                LblNota2.setText(Double.toString(ADC.getNotas(1)));
            } else {
                LblNota2.setText("-");
            }
            if (Existe(ADC.getNotas(2))) {
                LblNota3.setText(Double.toString(ADC.getNotas(2)));
            } else {
                LblNota3.setText("-");
            }
            if (Existe(ADC.getNotas(3))) {
                LblNota4.setText(Double.toString(ADC.getNotas(3)));
            } else {
                LblNota4.setText("-");
            }
            if (Existe(ADC.getPF())) {
                LblPromCurso.setText(Double.toString(ADC.getPF()));
            } else {
                LblPromCurso.setText("-");
            }
          
        }
    }//GEN-LAST:event_tblDetallesCursoMousePressed

