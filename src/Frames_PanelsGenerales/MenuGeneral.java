    private void BtnOpc1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOpc1ActionPerformed
        // TODO add your handling code here:
        switch (Usua.getRol()) {
            case "Administrador":

                if (VEPST.getEstadop().equals("Prematricula") || VEPST.getEstadop().equals("Matricula Abierta")) {
                    PanelRegistrarCursos pC = new PanelRegistrarCursos();
                    mostrarTipo(pC);
                } else {
                    if (E.LlamarPeriodosRegistrables() > 0) {
                        PanelRegistrarCursos pC = new PanelRegistrarCursos();
                        mostrarTipo(pC);
                    } else {
                        JOptionPane.showMessageDialog(null, "No hay periodos habilitados para Registrar Cursos");
                    }

                }
                break;
            case "Docente":
                if (VEPST.getEstadop().equals("Finalizado") || VEPST.getEstadop().equals("En Curso") || VEPST.getEstadop().equals("Matricula Abierta")) {
                    PanelVerCurso PVC = new PanelVerCurso();
                    mostrarTipo(PVC);
                } else {
                    JOptionPane.showMessageDialog(null, "Aun no inicia el ciclo actual, no puede ver sus cursos por ahora");
                }

                break;
            case "Alumno":
                if (VEPST.getEstadop().equals("Matricula Abierta")) {
                    PanelEmpezarmatricula PEM = new PanelEmpezarmatricula();
                    mostrarTipo(PEM);
                } else {
                    JOptionPane.showMessageDialog(null, "Matriculas cerradas por ahora");
                }

                break;

            default:
                System.out.println("ERROR");
        }

    }//GEN-LAST:event_BtnOpc1ActionPerformed

    private void BtnOpc2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOpc2ActionPerformed

        switch (Usua.getRol()) {
            case "Administrador":

                if (VEPST.getEstadop().equals("Prematricula") || VEPST.getEstadop().equals("Matricula Abierta")) {
                    Registro irReg = new Registro();
                    irReg.setVisible(true);
                    this.setEnabled(false);
                    irReg.addWindowListener(new WindowAdapter() {
                        @Override
                        public void windowClosed(WindowEvent e) {
                            setEnabled(true); // Habilitar la ventana principal cuando la ventana secundaria se cierra
                        }

                        public void windowDispose(WindowEvent e) {
                            setEnabled(true);
                        }
                    });
                } else {

                    JOptionPane.showMessageDialog(null, "El periodo actual no se encuentra habilitado para registrar usuarios");

                }

                break;
            case "Docente":
                if (VEPST.getEstadop().equals("En Curso") || VEPST.getEstadop().equals("Matricula Abierta")) {
                    PanelSolCambioDocente PSolD = new PanelSolCambioDocente();
                    mostrarTipo(PSolD);
                } else {

                    JOptionPane.showMessageDialog(null, "El periodo actual no se encuentra habilitado para solicitar un cambio");

                }

                break;
            case "Alumno":

                PanelDetallesCurso PDCA = new PanelDetallesCurso();
                mostrarTipo(PDCA);

                break;

            default:
                System.out.println("ERROR");
        }


    }//GEN-LAST:event_BtnOpc2ActionPerformed

