    private int porcero() {
        int valor;
        valor = (int) sp1.getValue();
        if (valor != 0) {
            valor = (int) sp2.getValue();
            if (valor != 0) {
                valor = (int) sp3.getValue();
                if (valor != 0) {
                    valor = (int) sp4.getValue();
                    if (valor != 0) {
                        valor = 1;
                    }
                }
            }
        }

        return valor;
    }

