    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        // TODO add your handling code here:
        int lleno, llenofinal = 0, porct;

        lleno = llenouno(txtNota1);
        llenofinal = llenofinal + lleno;
        lleno = llenouno(txtNota2);
        llenofinal = llenofinal + lleno;
        lleno = llenouno(txtNota3);
        llenofinal = llenofinal + lleno;
        lleno = llenouno(txtNota4);
        llenofinal = llenofinal + lleno;

        if (llenofinal == 4) {
            LlenarNotasPorc(listaI);
            LlenarNotasN(listaS);
            if (!sonElementosIguales(listaS)) {
                int val1 = (int) sp1.getValue();
                int val2 = (int) sp2.getValue();
                int val3 = (int) sp3.getValue();
                int val4 = (int) sp4.getValue();

                porct = val1 + val2 + val3 + val4;

                if (porct == 100) {
                    if (porcero() != 0) {

                        Secca.setPeriodoSeccion(LblPeriodoActual.getText());
                        Secca.setNombreSeccion((String) CmbCurso.getSelectedItem());
                        Secca.setCicloSeccion((int) cmbCiclo.getSelectedItem());
                        Secca.setSeccionID(Integer.parseInt(LblSeccion.getText()));
                        Secca.setEstadoSeccion("Abierto");
                        Secca.setNotasNombres(listaS);
                        Secca.setNotasPorcentajes(listaI);

                        cmbCiclo.setSelectedIndex(0);
                        CmbCurso.setSelectedIndex(0);

                        //limpiar JTextField
                        Limpiar(txtNota1);
                        Limpiar(txtNota2);
                        Limpiar(txtNota3);
                        Limpiar(txtNota4);
                        //Limpiar Spinner's
                        sp1.setValue(0);
                        sp2.setValue(0);
                        sp3.setValue(0);
                        sp4.setValue(0);

                        PanelRegistroHorario pRH = new PanelRegistroHorario();
                        mostrarTipo(pRH);
                    } else {
                        JOptionPane.showMessageDialog(null, "Uno de los porcentajes tiene valor 0%");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "La suma de los porcentajes no es igual al 100%");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Los nombres de las notas no pueden ser iguales");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Aun no se han completado todos los campos");
        }

    }//GEN-LAST:event_btnContinuarActionPerformed

