    private boolean sonElementosIguales(String[] arreglo) {
        for (int i = 0; i < arreglo.length - 1; i++) {
            for (int j = i + 1; j < arreglo.length; j++) {
                if (arreglo[i].equals(arreglo[j])) {
                    return true;
                }
            }
        }
        return false;
    }

