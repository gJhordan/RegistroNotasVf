    private void BtnBuscarDocenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarDocenteActionPerformed
        CmbDocente.removeAllItems();
        listaturnos[0] = (String) CmbSesion1.getSelectedItem();
        TransformarSlider(SldSesion1, 0);
        if (sesiones == 2) {
            TransformarSlider(SldSesion2, 1);
            listaturnos[1] = (String) CmbSesion2.getSelectedItem();
        }
        H.setDiasemana(listadias);
        H.setTurno(listaturnos);
        if (listaturnos[0].equals(listaturnos[1]) && listadias[0].equals(listadias[1])) {
            JOptionPane.showMessageDialog(null, "No puede registrar dos sesiones totalmente iguales");
            CmbDocente.setEnabled(false);
            LimpiarTabla(TblCurso);

        } else {
            int valor = DAOh.CargarDocentesxHorario(H, CmbDocente);

            if (valor > 0) {

                LimpiarTabla(TblCurso);
                CargarTabla(TblCurso);
                BtnRegHorario.setEnabled(true);
                CmbDocente.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "No hay docentes disponibles");
                BtnRegHorario.setEnabled(false);
                CmbDocente.setEnabled(false);
            }
        }

    }//GEN-LAST:event_BtnBuscarDocenteActionPerformed

