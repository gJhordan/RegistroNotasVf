    private String AsignarX(int i, String turno) {
        String valor = "";
        String turnoreal1, turnoreal2;
        turnoreal1 = (String) CmbSesion1.getSelectedItem();
        turnoreal2 = (String) CmbSesion2.getSelectedItem();
        if (sesiones == 1) {
            if (turno.equals(turnoreal1)) {
                if (i == (int) SldSesion1.getValue()) {
                    valor = "X";
                }
            }
        } else {
            if (turno.equals(turnoreal1)) {
                if (i == (int) SldSesion1.getValue()) {
                    valor = "X";
                }

            }
            if (turno.equals(turnoreal2)) {
                if (i == (int) SldSesion2.getValue()) {
                    valor = "X";
                }

            }
        }

        return valor;
    }

