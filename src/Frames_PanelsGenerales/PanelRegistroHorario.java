    private void TransformarSlider(JSlider JS, int orden) {
        int opc;
        opc = (int) JS.getValue();
        switch (opc) {
            case 1:
                listadias[orden] = "lunes";
                break;
            case 2:
                listadias[orden] = "martes";
                break;
            case 3:
                listadias[orden] = "miercoles";
                break;
            case 4:
                listadias[orden] = "jueves";
                break;
            case 5:
                listadias[orden] = "viernes";
                break;
            case 6:
                listadias[orden] = "sabado";
                break;
            default:
                throw new AssertionError();
        }
    }

